  ADC1->SMPR |= ADC_SMPR_SMP; /* (4) */
  ADC->CCR |= ADC_CCR_TSEN; /* (5) */

  #define TEMP130_CAL_ADDR ((uint16_t*) ((uint32_t) 0x1FF8007E))
  #define TEMP30_CAL_ADDR ((uint16_t*) ((uint32_t) 0x1FF8007A))
  #define VDD_CALIB ((uint16_t) (300))
  #define VDD_APPLI ((uint16_t) (330))

  int32_t ComputeTemperature(uint32_t measure)
  {
    int32_t temperature;
    temperature = ((measure * VDD_APPLI / VDD_CALIB) - (int32_t) *TEMP30_CAL_ADDR );
    temperature = temperature * (int32_t)(130 - 30);
    temperature = temperature / (int32_t)(*TEMP130_CAL_ADDR - *TEMP30_CAL_ADDR);
    //temperature = temperature + 30;
    return(temperature);
  }

  HAL_ADC_Start(&hadc);

  uint32_t mTemperature = 0;

  HAL_ADC_PollForConversion(&hadc, HAL_MAX_DELAY);
  mTemperature = ComputeTemperature(HAL_ADC_GetValue(&hadc));

  HAL_UART_Transmit(&huart2, "\033[0;0H", strlen("\033[0;0H"), HAL_MAX_DELAY);
  HAL_UART_Transmit(&huart2, "\033[2J", strlen("\033[2J"), HAL_MAX_DELAY);

  char value[30];

  sprintf(value, "VALUE: %ld", mTemperature);
  HAL_UART_Transmit(&huart2, value, strlen(value), HAL_MAX_DELAY);
  //  HAL_DMA_Start(&hdma_usart2_tx, (uint32_t)msg, (uint32_t)&huart2.Instance->TDR, strlen(msg));
  // HAL_DMA_Start_IT(&hdma_usart2_tx, &mTemperature, (uint32_t)&huart2.Instance->TDR, strlen(&mTemperature));
  // huart2.Instance->CR3 |= USART_CR3_DMAT;

